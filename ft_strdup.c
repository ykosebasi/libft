#include "libft.h"

char	*ft_strdup(const char *s)
{
	char			*ptr;
	size_t			total_size;
	size_t			i;

	total_size = ft_strlen(s) + 1;
	ptr = malloc(total_size);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
